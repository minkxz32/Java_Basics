package into_java_basics;


public class primitive_data_types {
    
     public static void main(String[] args) {
        double pi = 3.14159;      // A double-precision floating-point variable
        String greeting = "Hello, World!";  // A String variable
        boolean isJavaFun = true;  // A boolean variable
            System.out.println("The value of pi is: " + pi);
            System.out.println("The greeting is: " + greeting);
            System.out.println("Is Java fun? " + isJavaFun);
     }
}
